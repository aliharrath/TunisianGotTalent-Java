package com.pidev.entities;

import java.util.Objects;

/**
 *
 * @author Khaled
 */
public class test {
    private int id ;
    private String nom ;
    private int age ;

    public test(String nom, int age) {
        this.nom = nom;
        this.age = age;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "test{" + "nom=" + nom + ", age=" + age + '}';
    }
    
}
